<!-- do   !-->
<br />
<div class="h-container">
    <h2>do</h2>
</div>
<br />
<p>
   Выполняет действия описанные в указанной секции.<br />
   Как использовать:<br />
   <ul>
       <li>Создайте новую секцию в конфиге, имя может быть любым * </li>
       <li>Пропишите необходимые команды</li>
       <li>Запустите секцию на выполнения исползуя команду <b>do</b></li>
   </ul>
   Первый параметр - название секции, второй - сколько раз ее выполнить.
   <br />Пример записи:
<pre>

    [tasks]
    after_fight_delay = 1
    set_battle_config = set1

    do = mix_mob-10

    [mix_mob]
    run_battle = 1-1
    run_battle = 2-1
    run_battle = 3-1

    [set1]
    combo	= 1-3-1-3-3-2-3-3-2
    blocks	= alw

</pre>
    Вы можете использовать все доступные команды, секции <b>[tasks]</b>
    и <b>[mix_mob]</b> - равнозначны. Отличие в том, что с секции <b>[tasks]</b>
    бот начнет свою работу. Вы можете создать сколько угодно секций и вызывать их
    когда Вам нужно. Вы также можете использовать команду <b>do</b> внутри 
    описанных вами секций.
    <br />
</p>
<div class="alert">
    <b>Внимание!</b> Имя новой секции не должно совпадать с уже существующими!
</div>
<div class="alert">
    <b>Внимание!</b> Вы можете вызвать секцию внутри себя. Это может привести к 
    зацикливанию. Будте аккуратны, при описании рекурсивных алгоритмов.
</div>



<!-- do_file, do_file_section   !-->
<br />
<div class="h-container">
    <h2>do_file, do_file_section</h2>
</div>
<br />
<p>
   <b>do_file_section</b> - то же самое что и команда <b>do</b>, только позволяет
   выполнить секцию из другого файла.<br />
   Первый параметр - путь к файлу, второй - название секции, третий - сколько 
   раз выполнить.
   <br />Пример записи:
<pre>

    [tasks]
    do_file_section = mydevconfig.ini-mix_mob-20

</pre>
    <br />
</p>
<p>
   <b>do_file</b> - выполняет команды описанные в файле (в секции <b>[tasks]</b>)
   Первый параметр - путь к файлу, второй - сколько раз выполнить.
   <br />Пример записи:
<pre>

    [tasks]
    do_file = configs/quest1.ini-1

</pre>
    <br />
</p>
<div class="alert">
    <b>Внимание!</b> Файлы должны иметь  разширение .ini.
    Формат файла - такой же как и у config.ini.
    Путь к файлу - относительный.
</div>

