
<!-- use_power_regen !-->
<br />
<div class="h-container">
    <h2>use_power_regen</h2>
</div>
<br />
<p>
   Эта команда позволяет включить или отключить автоматическое восстановление 
   выносливости. Если включить, бот будет ходит в Стагород, покупать Вигор и 
   виписать его, по заданию.<br />
   Пример записи:
<pre>

    [tasks]
    use_power_regen = 1

</pre>
    1 - бот восстанавливает вынос, 0 - не восстанавливает.<br />
    
</p>
<div class="alert">
    <b>Внимание!</b> Если вы не используете перемещение по локациям - восстановление
    выносливости работать не будет, так как бот не будет знать где он находится.
</div>

    
<!-- after_fight_delay !-->
<br />
<div class="h-container">
    <h2>after_fight_delay</h2>
</div>
<br />
<p>
    Задает задержку после боя (в секундах).
   <br />Пример записи:
<pre>

    [tasks]
    after_fight_delay = 10

</pre>
    При after_fight_delay 10 - бот будет после каждого боя ждать 10 секунд.
     Минимальное значение - одна секунда<br />
</p>


<!-- set_battle_config !-->
<br />
<div class="h-container">
    <h2>set_battle_config</h2>
</div>
<br />
<p>
    Задает поведение бота в бою. В качестве параметра указываем имя секции, 
    с указанием последовательности блоков, ударов, использования
    эликсиров. 
   <br />Пример записи:
<pre>

    [tasks]
    set_battle_config = set1
    
    [set1]
    combo	= 1-3-1-3-3-2-3-3-2
    blocks	= alw

</pre>
    Таких секций как <b>[set1]</b> может быть сколько угодно, вы можете сами 
    придумать для них названия и с помошью команды <b>set_battle_config</b> 
    переключаться между ними в любой, удобный для вас момент<br />
</p>


<!-- run_battle !-->
<br />
<div class="h-container">
    <h2>run_battle</h2>
</div>
<br />
<p>
    Запускает функцию боя. Первый параметр - с кем бот проведет бой, второй
    - сколько раз 
   <br />Пример записи:
<pre>

    [tasks]
    set_battle_config = set1
    run_battle = 1-1
    
    [set1]
    combo	= 1-3-1-3-3-2-3-3-2
    blocks	= alw

</pre>
    Возможные варианты:<br/>
    run_battle = dueles-1 - бот проведет одну дуель.<br/>
    run_battle = 3-10 - бот проведет 10 боев с третим мобом, в локации в которой находится.
    <br />
</p>
<div class="alert">
    <b>Внимание!</b> Обязательно перед вызовом run_battle задайте боту поведение 
    в бою, используя команду set_battle_config
</div>


<!-- screen_delay  !-->
<br />
<div class="h-container">
    <h2>screen_delay</h2>
</div>
<br />
<p>
   Эта команда задает время(в миллисекундах) через которое бот будет делать 
   скриншот с окна игры. Возможно, Вам она никогда не понажобится. Есть смысл 
   ее использовать если у Вас слабый комп, или при включении бота зависает эмулятор.
   <br />Пример записи:
<pre>

    [tasks]
    screen_delay = 500.

</pre>
    Чем больше значени screen_delay, тем реже бот будет делать скрины.<br />
    При screen_delay 500 бот будет делать 2 сриншота в секунду.<br />
    При screen_delay 1000 бот будет делать 1 сриншот в секунду.<br />
    <br />
    Эта команда является необязательной, если явно не указывать данную задержку,
    то ей присвоится значение по умолчанию - 200 (5 скринов в секунду)
    <br />
</p>


